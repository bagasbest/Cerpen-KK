package com.cerpenkimia.koloid.quiz.score;

public class ScoreModel {

    private String name;
    private String score;

    public ScoreModel(){}

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getScore() {
        return score;
    }

    public void setScore(String score) {
        this.score = score;
    }
}
